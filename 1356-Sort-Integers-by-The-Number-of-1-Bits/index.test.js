const sortByBits = require('./index');

it('[0,1,2,3,4,5,6,7,8] should return [0,1,2,4,8,3,5,6,7]', () => {
  expect(sortByBits([0,1,2,3,4,5,6,7,8])).toStrictEqual([0,1,2,4,8,3,5,6,7]);
});

it('[1024,512,256,128,64,32,16,8,4,2,1] should return [1,2,4,8,16,32,64,128,256,512,1024]', () => {
  expect(sortByBits([1024,512,256,128,64,32,16,8,4,2,1])).toStrictEqual([1,2,4,8,16,32,64,128,256,512,1024]);
});

it('[1024,512,256,128,64,32,16,8,4,2,1] should return [1,2,4,8,16,32,64,128,256,512,1024]', () => {
  expect(sortByBits([1024,512,256,128,64,32,16,8,4,2,1])).toStrictEqual([1,2,4,8,16,32,64,128,256,512,1024]);
});

it('[10000,10000] should return [10000,10000]', () => {
  expect(sortByBits([10000,10000])).toStrictEqual([10000,10000]);
});

it('[2,3,5,7,11,13,17,19] should return [2,3,5,7,11,13,17,19]', () => {
  expect(sortByBits([2,3,5,17,7,11,13,19])).toStrictEqual([2,3,5,17,7,11,13,19]);
});